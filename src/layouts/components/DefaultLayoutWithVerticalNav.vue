<script lang="ts" setup>
import { useRouter, useRoute } from 'vue-router';
const router = useRouter();
const route = useRoute();

import { useLoginDataStore } from '@/stores/loginData';
const loginData = useLoginDataStore();

import { useUserSettingsStore } from '@/stores/API Data/userSettings';
const userSettingsStore = useUserSettingsStore();

import { useAppState } from '@/stores/appState';
const appState = useAppState();

import VerticalNavLayout from '@layouts/components/VerticalNavLayout.vue';
import VerticalNavLink from '@layouts/components/VerticalNavLink.vue';
import VerticalNavSectionTitle from '@/@layouts/components/VerticalNavSectionTitle.vue';

// Components
import Footer from '@/layouts/components/Footer.vue';
import NavbarThemeSwitcher from '@/layouts/components/NavbarThemeSwitcher.vue';
import UserProfile from '@/layouts/components/UserProfile.vue';
import SelectedGuild from '@/components/SelectedGuild.vue';

onMounted(async () => {
	// Fetch user's bot settings
	if (!userSettingsStore.userSettings) await userSettingsStore.fetch(loginData?.userData?.id);
	// If user has opted out of bot interactions, only let them access Account Settings
	if (userSettingsStore.userSettings?.disableBot && route.path !== '/account-settings') router.push({ name: 'account-settings' });
});

watch(route, () => {
	// If user has opted out of bot interactions, only let them access Account Settings
	if (userSettingsStore.userSettings?.disableBot && route.path !== '/account-settings') router.push({ name: 'account-settings' });
	// Force a logout -> login redirect if loginData isn't present or usable
	if (!loginData.userData || !loginData.userData.id) router.push({ name: 'logout' });
});
</script>

<template>
	<VerticalNavLayout>
		<!-- ðŸ‘‰ navbar -->
		<template #navbar="{ toggleVerticalOverlayNavActive }">
			<div class="d-flex h-100 align-center">
				<!-- ðŸ‘‰ Vertical nav toggle in overlay mode -->
				<IconBtn class="ms-n3 d-lg-none" @click="toggleVerticalOverlayNavActive(true)">
					<VIcon icon="mdi-menu" />
				</IconBtn>

				<SelectedGuild v-if="appState.selectedGuild" v-bind="{ guildData: appState.selectedGuild }" />

				<VSpacer />

				<IconBtn class="me-2" href="https://rtbyte.xyz/discord" target="_blank" rel="noopener noreferrer">
					<VIcon icon="ic-baseline-discord" />
					<VTooltip activator="parent" open-delay="1000" scroll-strategy="close">
						RTByte Support Discord
					</VTooltip>
				</IconBtn>

				<IconBtn class="me-2" @click="loginData.refreshData()">
					<VIcon icon="mdi-refresh" />
					<VTooltip activator="parent" open-delay="1000" scroll-strategy="close">
						Refresh Discord Data
					</VTooltip>
				</IconBtn>

				<NavbarThemeSwitcher class="me-2" />

				<UserProfile v-if="loginData.userData" v-bind="{ userData: loginData.userData }" />
			</div>
		</template>

		<template #vertical-nav-content>
			<VerticalNavLink :item="{
				title: 'Servers',
				icon: 'mdi-home-outline',
				to: '/guilds',
			}" />
			<VerticalNavLink :item="{
				title: 'Account Settings',
				icon: 'mdi-account-cog-outline',
				to: '/account-settings',
			}" />

			<!-- ðŸ‘‰ Pages -->
			<VerticalNavSectionTitle :item="{
				heading: 'Bot Settings',
			}" />
			<div>
				<VTooltip activator="parent" v-if="!appState.selectedGuild" location="bottom">
					Select a Server to access Settings
				</VTooltip>
				<VerticalNavLink :item="{
					title: 'Server Settings',
					icon: 'ic-baseline-discord',
					to: '/settings'
				}" :class="appState.selectedGuild ? undefined : 'disabled'" />
				<VerticalNavLink :item="{
					title: 'Info Logs Settings',
					icon: 'mdi-form-select',
					to: '/settings/info-logs',
				}" :class="appState.selectedGuild ? undefined : 'disabled'" />
			</div>
			<!-- ðŸ‘‰ User Interface -->
			<VerticalNavSectionTitle :item="{
				heading: 'User Interface',
			}" />
			<VerticalNavLink :item="{
				title: 'Typography',
				icon: 'mdi-alpha-t-box-outline',
				to: '/typography',
			}" />
			<VerticalNavLink :item="{
				title: 'Icons',
				icon: 'mdi-eye-outline',
				to: '/icons',
			}" />
			<VerticalNavLink :item="{
				title: 'Cards',
				icon: 'mdi-credit-card-outline',
				to: '/cards',
			}" />
			<VerticalNavLink :item="{
				title: 'Tables',
				icon: 'mdi-table',
				to: '/tables',
			}" />
			<VerticalNavLink :item="{
				title: 'Form Layouts',
				icon: 'mdi-form-select',
				to: '/form-layouts',
			}" />
		</template>

		<!-- ðŸ‘‰ Pages -->
		<slot />

		<!-- ðŸ‘‰ Footer -->
		<template #footer>
			<Footer />
		</template>
	</VerticalNavLayout>
</template>

